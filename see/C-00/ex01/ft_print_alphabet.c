#include<unistd.h>

/*	ASCII a = 97
 *	ASCII z = 122*/

void	ft_print_alphabet(void)
{
	char	a;

	a = 97;
	while (a <= 122)
	{	
		write(1, &a, 1);
		a++;
	}
}
