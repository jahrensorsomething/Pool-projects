int	ft_sqrt(int nb)
{
	int	a;

	a = 0;
	if (a < 0 || a > 2147483647)
		return (0);
	else
	{
		while (a * a < nb && a < 46340)
		{
			a++;
		}
		if (a * a == nb)
			return (a);
		else
			return (0);
	}
}
